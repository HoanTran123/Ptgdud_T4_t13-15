//data 1
var Dolphins = [96,108,89]
console.log("Các điểm của Dolphins : " +Dolphins)
var Koalas =[88,91,100]
console.log("Các điểm của Koalas : " +Koalas)

var avgDolphins = (Dolphins[0] + Dolphins[1] + Dolphins[2])/3
console.log("Điểm trung bình của Dolphins : " +avgDolphins)
var avgKoalas = (Koalas[0] + Koalas[1] + Koalas[2])/3
console.log("Điểm trung bình của Koalas : " +avgKoalas)
if(avgDolphins > avgKoalas){
    console.log("điểm trung bình của Dolphins cao hơn Koalas")
}else if(avgDolphins == avgKoalas){
    console.log("điểm trung bình của Dolphins bằng Koalas")
}else{
    console.log("điểm trung bình của Dolphins thấp hơn Koalas")
}
